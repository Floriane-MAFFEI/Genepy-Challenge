# Écris un programme Python affichant le nombre de secondes dans 365 jours.

# Tu auras besoin de la fonction print pour afficher le résultat.

# Tu auras aussi besoin de l'opérateur * pour effectuer des multiplications.

# Par exemple, ce code affiche le nombre de secondes dans une journée :

# print(60 * 60 * 24)

